package com.predicto.model;

public class User {
	
	private int id;
	private String username;
	private String password;
	private String email;
	private int age;
	private int height;
	private int weight;
	private int gender;
	private int diabetic;
	private int smoker;
	private int alcoholic;
	private String heredity;
	private int status;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public int getDiabetic() {
		return diabetic;
	}
	public void setDiabetic(int diabetic) {
		this.diabetic = diabetic;
	}
	public int getSmoker() {
		return smoker;
	}
	public void setSmoker(int smoker) {
		this.smoker = smoker;
	}
	public int getAlcoholic() {
		return alcoholic;
	}
	public void setAlcoholic(int alcoholic) {
		this.alcoholic = alcoholic;
	}
	public String getHeredity() {
		return heredity;
	}
	public void setHeredity(String heredity) {
		this.heredity = heredity;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
}
